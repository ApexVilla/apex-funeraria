// Aplica o tema salvo antes do primeiro paint para evitar flash e manter o
// modo escuro ao reabrir o app (antes só era aplicado ao visitar o Perfil).
const themeInitScript = `try{if(localStorage.getItem("fenix-portal-theme")==="dark"){document.documentElement.classList.add("dark")}}catch(e){}`;

function RootShell({ children }: { children: ReactNode }) {
  return (
    <html lang="pt-BR">
      <head>
        <script dangerouslySetInnerHTML={{ __html: themeInitScript }} />
        <HeadContent />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

import { Toaster } from "../components/ui/sonner";

function RootComponent() {
  const { queryClient } = Route.useRouteContext();

  return (
    <QueryClientProvider client={queryClient}>
      {/* Required: nested routes render here. Removing <Outlet /> breaks all child routes. */}
      <Outlet />
      <Toaster position="top-center" closeButton richColors />
    </QueryClientProvider>
  );
}
